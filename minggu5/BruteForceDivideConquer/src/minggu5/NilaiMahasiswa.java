package minggu5;

public class NilaiMahasiswa {
    String nama;
    String nim;
    int tahunMsk;
    int nilaiUTS;
    int nilaiUAS;

    NilaiMahasiswa(String nm, String nim, int tm, int nuts, int nuas) {
        nama = nm;
        this.nim = nim;
        tahunMsk = tm;
        nilaiUTS = nuts;
        nilaiUAS = nuas;
    }

}